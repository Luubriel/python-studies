from math import *

num = 10

num_2 = 23.45

print(str(num) + " MOD 3 = " + str(num % 3))

print("ABSOLUTE VALUE of " + str(num) + " = " + str(abs(num)))

print(str(num) + " to the power of 3 = " + str(pow(num, 3)))

print(str(max(num, 23)) + "\n" + str(min(5, num)))

print(floor(num_2))
print(ceil(num_2))

print("The square root of 25 is " + str(sqrt(25)))
